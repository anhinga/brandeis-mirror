int tryrus (int next, int RusCodes [], char * Translit [], FILE *out)
{
  int i;
  for (i=0; i<33; i++)
    {
      if (RusCodes [i] == next)
        break;
    }
  if (i==33)
    return 0;
  else
    {
      fprintf (out, "%s", Translit [i]);
      return 1;
    }
}

